    private void scheduleCourseSubmitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_scheduleCourseSubmitButtonActionPerformed
        // TODO add your handling code here:

        CourseEntry c = (CourseEntry) scheduleCourseCourseComboBox.getSelectedItem();
        StudentEntry s = (StudentEntry) scheduleCourseStudentComboBox.getSelectedItem();

        java.sql.Timestamp currentTimestamp = new java.sql.Timestamp(Calendar.getInstance().getTime().getTime());
        Integer d = ScheduleQueries.getScheduledStudentCount(currentSemesterComboBox.getSelectedItem().toString(),c.getCourseCode());
        if(c.getSeats() > d){
            ScheduleEntry sch = new ScheduleEntry(currentSemesterComboBox.getSelectedItem().toString(),c.getCourseCode(),s.getStudentID(),"scheduled",currentTimestamp);
            ScheduleQueries.addScheduleEntry(sch);
            scheduleCourseStatusLabel.setText("Course Scheduled: " +s.getLastName()+", " +s.getFirstName() + " - "+ c.getCourseCode());
        }
        else{
            ScheduleEntry sch = new ScheduleEntry(currentSemesterComboBox.getSelectedItem().toString(),c.getCourseCode(),s.getStudentID(),"waitlisted",currentTimestamp);
            ScheduleQueries.addScheduleEntry(sch);
            scheduleCourseStatusLabel.setText("Course WaitListed: "+s.getLastName()+", " +s.getFirstName() +" - "+ c.getCourseCode());
        }
        //rebuildCourseComboBoxes();

    }//GEN-LAST:event_scheduleCourseSubmitButtonActionPerformed

