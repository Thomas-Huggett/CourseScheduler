    private void DropCourseStudentSubmitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DropCourseStudentSubmitButtonActionPerformed
        // TODO add your handling code here:
        String s = (String) currentSemesterComboBox.getSelectedItem();
        StudentEntry student = (StudentEntry) DropCourseStudentComboBox1.getSelectedItem();
        String ID = student.getStudentID();
        CourseEntry c = (CourseEntry) DropCourseStudentCourseComboBox.getSelectedItem();
        String code = c.getCourseCode();
        ScheduleQueries.removeCourseEntry(ID,code,s);
        ArrayList<ScheduleEntry> students = ScheduleQueries.getScheduledStudentsByCourse(s, code);
        ArrayList<ScheduleEntry> waitlisted = new ArrayList<ScheduleEntry>();
        for(ScheduleEntry stu: students){
            if(stu.getStatus().equals("waitlisted")){
                waitlisted.add(stu);
            }
        }
        String textDisplay = "";
        textDisplay += student.getLastName() + ", " + student.getFirstName() +" "+student.getStudentID() + " has been dropped from " + c.getCourseCode() + "\n";
        Collections.sort(waitlisted);
        if(waitlisted.size() > 0){

            ScheduleQueries.MoveOffWaitlist(waitlisted.get(0));
            StudentEntry addedStudent = StudentQueries.getStudentByID(waitlisted.get(0).getStudentID());
            textDisplay += addedStudent.getLastName() + ", " + addedStudent.getFirstName() + " " +" has been scheduled into " + c.getCourseCode() + "\n";

            ScheduleQueries.addScheduleEntry(new ScheduleEntry(waitlisted.get(0).getSemester(),waitlisted.get(0).getCourseCode(),waitlisted.get(0).getStudentID(),"scheduled",waitlisted.get(0).getTimestamp()));
        }
        DropCourseStudentTextArea.setText(textDisplay);
    }//GEN-LAST:event_DropCourseStudentSubmitButtonActionPerformed

