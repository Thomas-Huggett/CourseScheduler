    public void rebuildCourseComboBoxes()
    {
        ArrayList<CourseEntry> Courses = CourseQueries.getAllCourses();
        
        
        if(currentSemesterComboBox.getSelectedItem() == null){
            
        }
        else{
        ArrayList<CourseEntry> Courseh = CourseQueries.getAllCourses(currentSemesterComboBox.getSelectedItem().toString());
        scheduleCourseCourseComboBox.setModel(new javax.swing.DefaultComboBoxModel(Courseh.toArray()));
        DropCourseCourseComboBox.setModel(new javax.swing.DefaultComboBoxModel(Courseh.toArray()));
        DisplayCourseListOfStudentsComboBox.setModel(new javax.swing.DefaultComboBoxModel(Courseh.toArray()));
        }
        
        String s = (String) currentSemesterComboBox.getSelectedItem();
        StudentEntry student = (StudentEntry) DropCourseStudentComboBox1.getSelectedItem();
        if(student != null){
        String ID = student.getStudentID();
        
        ArrayList<ScheduleEntry> classes = ScheduleQueries.getScheduleByStudent(s,ID);
        //ArrayList<ScheduleEntry> classes = ScheduleQueries.getScheduleByStudent(s,ID);
        ArrayList<CourseEntry> cs = new ArrayList<CourseEntry>();
        for(ScheduleEntry i: classes){
            cs.add(new CourseEntry(s,i.getCourseCode(),CourseQueries.getDescription(i.getCourseCode()),CourseQueries.getCourseSeats(s,i.getCourseCode())));
        }
        DropCourseStudentCourseComboBox.setModel(new javax.swing.DefaultComboBoxModel(cs.toArray()));
        }
    }
    public void updateCourseDisplay(){
        String smstr = (String) currentSemesterComboBox.getSelectedItem();
        String displayy = "";
        ArrayList<CourseEntry> coursess = CourseQueries.getAllCourses(smstr);
        for(CourseEntry cours: coursess){
            displayy += cours.getCourseCode()+ " - "+cours.getDescription()+" "+cours.getSeats()+" seats";
            displayy+= "\n";
        }
        displayCoursesTextArea.setText(displayy);
    }
