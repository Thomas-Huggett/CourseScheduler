    private void DisplayCourseListOfStudentsDisplayButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DisplayCourseListOfStudentsDisplayButtonActionPerformed
        // TODO add your handling code here:
        String s = (String) currentSemesterComboBox.getSelectedItem();
        CourseEntry c = (CourseEntry) DisplayCourseListOfStudentsComboBox.getSelectedItem();
        String cc = c.getCourseCode();
        ArrayList<ScheduleEntry> l = ScheduleQueries.getScheduledStudentsByCourse(s,cc);
        String courseDisplay = "Scheduled Students: \n";
        String scheduled = "";
        String waitlisted = "";
        displayCourseListOfStudentsTextArea.setText("L");
        ArrayList<ScheduleEntry> listed = new ArrayList<>();
        for(ScheduleEntry i: l){
            StudentEntry student = StudentQueries.getStudentByID(i.getStudentID());
            if(i.getStatus().equals("scheduled")){
                scheduled += student.getLastName() + ", " + student.getFirstName() + " "+ i.getStudentID() + "\n";
            }
            else{
                listed.add(i);
                waitlisted += student.getLastName() + ", " + student.getFirstName() + " "+ i.getStudentID() + "\n";
            }
        }
        courseDisplay += scheduled;
        courseDisplay += "Waitlisted Students: \n";
        Collections.sort(listed);
        for(ScheduleEntry i: listed){
            StudentEntry student = StudentQueries.getStudentByID(i.getStudentID());
            courseDisplay += student.getLastName() + ", " + student.getFirstName() + " "+ student.getStudentID() + "\n";
        }
        displayCourseListOfStudentsTextArea.setText(courseDisplay);

    }//GEN-LAST:event_DisplayCourseListOfStudentsDisplayButtonActionPerformed

